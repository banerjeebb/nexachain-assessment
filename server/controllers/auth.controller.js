/**
 * @file auth.controller.js
 * @description Handles user registration, login, and "current user" lookup.
 * All handlers respond with the standard envelope { success, data, message }.
 */

const jwt  = require('jsonwebtoken');
const User = require('../models/User.model');
const { JWT_EXPIRES_IN } = require('../config/constants');

/**
 * Signs a JWT carrying the user's Mongo `_id` as `id`.
 *
 * @param {mongoose.Types.ObjectId|string} id - User document ID.
 * @returns {string} Signed JWT, valid for `JWT_EXPIRES_IN`.
 */
const signToken = (id) =>
  jwt.sign({ id }, process.env.JWT_SECRET, { expiresIn: JWT_EXPIRES_IN });

/**
 * POST /api/auth/register
 *
 * Creates a new user account. If a `referralCode` is supplied, the matching
 * user is linked as the new account's referrer (`referredBy`). A unique
 * referral code for the new user is auto-generated by the User model's
 * pre-save hook.
 *
 * @param {express.Request} req - Body: { fullName, email, mobile, password, referralCode? }
 * @param {express.Response} res
 * @returns {Promise<void>} 201 with { token, user } on success;
 *   409 if the email is already registered; 400 if `referralCode` doesn't match any user.
 */
const register = async (req, res) => {
  try {
    const { fullName, email, mobile, password, referralCode } = req.body;

    const existing = await User.findOne({ email });
    if (existing)
      return res.status(409).json({ success: false, data: null, message: 'Email already registered' });

    let referredBy = null;
    if (referralCode) {
      const referrer = await User.findOne({ referralCode });
      if (!referrer)
        return res.status(400).json({ success: false, data: null, message: 'Invalid referral code' });
      referredBy = referrer._id;
    }

    const user  = await User.create({ fullName, email, mobile, password, referredBy });
    const token = signToken(user._id);

    return res.status(201).json({
      success: true,
      data: {
        token,
        user: {
          _id:           user._id,
          fullName:      user.fullName,
          email:         user.email,
          referralCode:  user.referralCode,
          walletBalance: user.walletBalance,
        },
      },
      message: 'Registration successful',
    });
  } catch (err) {
    console.error('[Auth] Register error:', err.message);
    return res.status(500).json({ success: false, data: null, message: 'Server error' });
  }
};

/**
 * POST /api/auth/login
 *
 * Authenticates a user by email/password and issues a fresh JWT.
 *
 * @param {express.Request} req - Body: { email, password }
 * @param {express.Response} res
 * @returns {Promise<void>} 200 with { token, user } on success;
 *   401 if credentials are invalid; 403 if the account is not `active`.
 */
const login = async (req, res) => {
  try {
    const { email, password } = req.body;

    const user = await User.findOne({ email });
    if (!user || !(await user.comparePassword(password)))
      return res.status(401).json({ success: false, data: null, message: 'Invalid email or password' });

    if (user.accountStatus !== 'active')
      return res.status(403).json({ success: false, data: null, message: 'Account is not active' });

    const token = signToken(user._id);

    return res.status(200).json({
      success: true,
      data: {
        token,
        user: {
          _id:           user._id,
          fullName:      user.fullName,
          email:         user.email,
          referralCode:  user.referralCode,
          walletBalance: user.walletBalance,
        },
      },
      message: 'Login successful',
    });
  } catch (err) {
    console.error('[Auth] Login error:', err.message);
    return res.status(500).json({ success: false, data: null, message: 'Server error' });
  }
};

/**
 * GET /api/auth/me
 *
 * Returns the profile of the currently authenticated user. Requires the
 * `auth` middleware to have populated `req.user`.
 *
 * @param {express.Request} req - `req.user` set by auth middleware.
 * @param {express.Response} res
 * @returns {Promise<void>} 200 with the user document as `data`.
 */
const getMe = async (req, res) => {
  return res.status(200).json({
    success: true,
    data:    req.user,
    message: 'User profile fetched',
  });
};

module.exports = { register, login, getMe };
